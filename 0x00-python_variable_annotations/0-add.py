#!/usr/bin/env python3


def add(a: float, b: float) -> float:
    """
    Returns the sum of two float numbers.

    Args:
    a (float): First float number.
    b (float): Second float number.

    Returns:
    float: The sum of `a` and `b`.

    Example:
    >>> add(1.0, 2.0)
    3.0
    """
    return a + b
