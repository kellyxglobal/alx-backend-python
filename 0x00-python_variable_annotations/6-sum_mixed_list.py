#!/usr/bin/env python3


def sum_mixed_list(mxd_lst: list[int or float]) -> float:
    """
    This function takes a list of integers and floats
    and returns their sum as a float

    Args:
    - mxd_lst: a list of integers and floats

    Returns:
    - The sum of the elements in mxd_lst as a float
    """
    return sum(mxd_lst)
